package fr.xebia.demo.jmx.webservice;

public class Status404Exception extends Exception {

    public Status404Exception() {
        super();
    }

    public Status404Exception(String message, Throwable cause) {
        super(message, cause);
    }

    public Status404Exception(String message) {
        super(message);
    }

    public Status404Exception(Throwable cause) {
        super(cause);
    }

}
